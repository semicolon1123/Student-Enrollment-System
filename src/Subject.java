    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // Checks if there is any selected data to be deleted 
        if (txtField_subject_id.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please select data to delete");
        } else {
            // Creating a confirmDialog for confirming data deletion
            int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this data?","Delete",JOptionPane.YES_NO_OPTION);
            
            // p = 0 means yes / p = -1 means no
            if (p == 0){
              try {
                  // MySQL query for deleting single record / row 
                  String sql = "DELETE FROM subject WHERE subj_id = ?";
                  pst = conn.prepareStatement(sql);
                  pst.setString(1, txtField_subject_id.getText());
                  pst.execute();
                  // Display this message after successfully running DELETE query
                  JOptionPane.showMessageDialog(null, "Data deleted successfully!");
                  
              } catch (Exception e){
                  JOptionPane.showMessageDialog(null, e);
              } finally {
                  try{
                      // Closing the connection
                      rs.close();
                      pst.close();
                  } catch (Exception e){
                      
                  }
              } // end of finally block
            } 
        } // end of else statement
            clearFields();
            updateTable();
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        // Writing code for data input validation
        String subj_id_validate = txtField_subject_id.getText();
        String subjectDesc_validate = txtField_subject_desc.getText();
        String units_validate = txtField_units.getText();
        String room_validate = txtField_room.getText();
        String instructor_validate = txtField_instructor.getText();
      
        // Using if-else conditions for validation
        if (subj_id_validate.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter your subject id");
        } else if(subjectDesc_validate.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the subject description");
        } else if(units_validate.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the number of units");
        } else if(cb_schedule.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null, "Please enter select the schedule");
        } else if (room_validate.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the room");
        } else if (instructor_validate.equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the name of the Instructor");
        }
        
        else {
           
        // If all data validations have been assigned a value then run 
        try {
            // Writing SQL query and storing in sql String variable
            String sql = "INSERT INTO subject (subj_id, subj_desc, units, schedule, room, instructor) VALUES (?, ?, ?, ?, ?, ?);";
            // Oject conn is used to create connection between java application and MySQL database and will execute the query
            pst = conn.prepareStatement(sql);
            
            // Getting data to INSERT INTO the database
            pst.setString(1, txtField_subject_id.getText());
            pst.setString(2, txtField_subject_desc.getText());
            pst.setString(3, txtField_units.getText());
            
            String schedule_value = cb_schedule.getSelectedItem().toString();
            pst.setString(4, schedule_value);
            
            pst.setString(5, txtField_room.getText());
            pst.setString(6, txtField_instructor.getText());
            
            // execute() method used to execute the SQL query
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Saved Successfully!");
            
        } catch (Exception e){
            
            JOptionPane.showMessageDialog(null, e);
            
        } finally {
            try {
                // Closing the connection in both ResultSet and PreparedStatement
                rs.close();
                pst.close();
            } catch (Exception e){
                
            }
        } // end of finally block
        } // end of else block
        
        // Updating data in JTable after saving 
        updateTable();
        // Clearing all the fields after saving
        clearFields();
        
        // Button Settings
        btn_save.setEnabled(false);
        btn_new.setEnabled(true);
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_clear.setEnabled(true);
    }//GEN-LAST:event_btn_saveActionPerformed

