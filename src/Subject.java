    private void table_subject_infoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_subject_infoMouseClicked
        
        int row = table_subject_info.getSelectedRow();
        String tableClick = (table_subject_info.getModel().getValueAt(row, 0).toString());
        
        try {
            
            String sql = "SELECT * FROM subject WHERE subj_id = " + tableClick;
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if (rs.next()){
                String str_subjID = rs.getString("subj_id");
                txtField_subject_id.setText(str_subjID);
                
                String str_subj_desc = rs.getString("subj_desc");
                txtField_subject_desc.setText(str_subj_desc);
                
                String str_units = rs.getString("units");
                txtField_units.setText(str_units);
                
                String str_schedule = rs.getString("schedule");
                cb_schedule.setSelectedItem(str_schedule);
                
                String str_room = rs.getString("room");
                txtField_room.setText(str_room);   
                
                String str_instructor = rs.getString("instructor");
                txtField_instructor.setText(str_instructor);
                  
            } // end if rs.next() method
        } catch (Exception e){
            
            JOptionPane.showMessageDialog(null, e);
            
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e){
                
            }
            
        }
        
        // Button Settings
        btn_save.setEnabled(false);
        btn_new.setEnabled(true);
        btn_update.setEnabled(true);
        btn_delete.setEnabled(true);
        btn_clear.setEnabled(true);
    }//GEN-LAST:event_table_subject_infoMouseClicked

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        
        try {
            String sql = "UPDATE subject SET subj_desc = ?, units = ?, schedule = ?, room = ?, instructor =? WHERE subj_id = ?";
            pst = conn.prepareStatement(sql);
            
            pst.setString(1, txtField_subject_desc.getText());
            pst.setString(2, txtField_units.getText());
            
            String schedule_value = cb_schedule.getSelectedItem().toString();
            pst.setString(3, schedule_value);
            
            pst.setString(4, txtField_room.getText());
            pst.setString(5, txtField_instructor.getText());
            pst.setString(6, txtField_subject_id.getText());
            
            // execute() method used to execute the SQL query
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data Updated Successfully!");
            
        } catch (Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                // Closing the connection
                pst.close();
                rs.close();
            } catch (Exception e){
                
            }
            
        } // end of finally block
        updateTable();
    }//GEN-LAST:event_btn_updateActionPerformed

